    @Override
    @Transactional
    public ClinicalReport generateDiagnosticReport(Long consultationRecordId) {
        long startTime = System.currentTimeMillis();

        try {
            log.info("📋 진단 보고서 생성 시작: recordId={}", consultationRecordId);
            String tenantId = TenantContextHolder.getRequiredTenantId();
            ConsultationRecord record = consultationRecordRepository.findByTenantIdAndId(tenantId, consultationRecordId)
                .orElseThrow(() -> new IllegalArgumentException("상담 기록을 찾을 수 없습니다."));

            String systemPrompt = getDiagnosticExpertSystemPrompt();
            String userPrompt = buildDiagnosticReportPrompt(record);

            AIResponse aiResponse = geminiModelProvider.analyze(
                systemPrompt, userPrompt, 3000, 0.2); // 진단은 보수적으로 (낮은 temperature)

            if (!aiResponse.isSuccess()) {
                throw new RuntimeException("AI 분석 실패: " + aiResponse.getErrorMessage());
            }

            long processingTime = System.currentTimeMillis() - startTime;

            // JSON 응답 파싱
            DiagnosticSections sections = parseDiagnosticResponse(aiResponse.getContent());

            ClinicalReport report = ClinicalReport.builder()
                .consultationRecordId(consultationRecordId)
                .reportType("DIAGNOSTIC")
                .reportFormat("DIAGNOSTIC")
                .diagnosisSummary(sections.summary)
                .diagnosticImpressions(sections.impressions)
                .treatmentRecommendations(sections.recommendations)
                .autoGenerated(true)
                .humanReviewed(false)
                .aiModelUsed(geminiModelProvider.getModelName())
                .generationTimeMs((int) processingTime)
                .build();

            report.setTenantId(record.getTenantId());

            report = clinicalReportRepository.save(report);

            log.info("✅ 진단 보고서 생성 완료: reportId={}, time={}ms", report.getId(), processingTime);

            return report;

        } catch (Exception e) {
            log.error("❌ 진단 보고서 생성 실패: recordId={}, error={}",
                consultationRecordId, e.getMessage(), e);
            throw new RuntimeException("진단 보고서 생성 실패: " + e.getMessage(), e);
        }
    }

    @Override
    public ClinicalReport getClinicalReport(Long reportId) {
        return clinicalReportRepository.findByIdAndIsDeletedFalse(reportId)
            .orElseThrow(() -> new IllegalArgumentException("임상 보고서를 찾을 수 없습니다: " + reportId));
    }

    @Override
    @Transactional
    public ClinicalReport updateClinicalReport(Long reportId, ClinicalReport updatedReport) {
        ClinicalReport report = getClinicalReport(reportId);

        // 업데이트 가능한 필드만 수정
        if (updatedReport.getSubjectiveText() != null) {
            report.setSubjectiveText(updatedReport.getSubjectiveText());
        }
        if (updatedReport.getObjectiveText() != null) {
            report.setObjectiveText(updatedReport.getObjectiveText());
        }
        if (updatedReport.getAssessmentText() != null) {
            report.setAssessmentText(updatedReport.getAssessmentText());
        }
        if (updatedReport.getPlanText() != null) {
            report.setPlanText(updatedReport.getPlanText());
        }
        if (updatedReport.getDataText() != null) {
            report.setDataText(updatedReport.getDataText());
        }
        if (updatedReport.getDiagnosisSummary() != null) {
            report.setDiagnosisSummary(updatedReport.getDiagnosisSummary());
        }
        if (updatedReport.getDiagnosticImpressions() != null) {
            report.setDiagnosticImpressions(updatedReport.getDiagnosticImpressions());
        }
        if (updatedReport.getTreatmentRecommendations() != null) {
            report.setTreatmentRecommendations(updatedReport.getTreatmentRecommendations());
        }

        return clinicalReportRepository.save(report);
    }

    @Override
    @Transactional
    public ClinicalReport approveClinicalReport(Long reportId, Long reviewerUserId) {
        ClinicalReport report = getClinicalReport(reportId);
        report.markAsReviewed(reviewerUserId);
        return clinicalReportRepository.save(report);
    }

