    @Override
    @Transactional
    public ClinicalReport generateSOAPNote(AudioTranscription transcription, ConsultationRecord record) {
        long startTime = System.currentTimeMillis();

        try {
            log.info("📝 SOAP 노트 생성 시작: recordId={}", record.getId());

            // 시스템 프롬프트
            String systemPrompt = getClinicalExpertSystemPrompt();

            // 사용자 프롬프트
            String userPrompt = buildSOAPPrompt(transcription, record);

            // Gemini API 호출
            AIResponse aiResponse = geminiModelProvider.analyze(
                systemPrompt, userPrompt, 2000, 0.3);

            if (!aiResponse.isSuccess()) {
                log.error("❌ Gemini API 호출 실패: {}", aiResponse.getErrorMessage());
                throw new RuntimeException("AI 분석 실패: " + aiResponse.getErrorMessage());
            }

            long processingTime = System.currentTimeMillis() - startTime;

            // JSON 응답 파싱
            SOAPSections sections = parseSOAPResponse(aiResponse.getContent());

            // 보고서 엔티티 생성
            ClinicalReport report = ClinicalReport.builder()
                .consultationRecordId(record.getId())
                .reportType("SOAP")
                .reportFormat("SOAP")
                .subjectiveText(sections.subjective)
                .objectiveText(sections.objective)
                .assessmentText(sections.assessment)
                .planText(sections.plan)
                .autoGenerated(true)
                .humanReviewed(false)
                .aiModelUsed(geminiModelProvider.getModelName())
                .generationTimeMs((int) processingTime)
                .confidenceScore(calculateConfidenceScore(sections))
                .build();

            report.setTenantId(record.getTenantId());

            report = clinicalReportRepository.save(report);

            log.info("✅ SOAP 노트 생성 완료: reportId={}, time={}ms", report.getId(), processingTime);

            return report;

        } catch (Exception e) {
            log.error("❌ SOAP 노트 생성 실패: recordId={}, error={}",
                record.getId(), e.getMessage(), e);
            throw new RuntimeException("SOAP 노트 생성 실패: " + e.getMessage(), e);
        }
    }

