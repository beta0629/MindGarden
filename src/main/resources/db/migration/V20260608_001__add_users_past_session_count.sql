--
-- 입력 보고서: 사용자 요청 (2026-06-04)
--   "내담자 등록 정보에 과거 회기수 등록하도록 필드를 추가하고,
--    입력된 회기와 현재 카운트 된 회기를 더해서 보여주고,
--    일정 상세 페이지에 총회기수 = 입력된 회기 + 현재 카운트된 회기 노출.
--    이건 과거 회기수를 상담사가 알기 위함. null 값 허용,
--    신규 내담자 카운트는 현재대로 진행."
--
-- 본 마이그레이션은:
--   1. users 테이블에 past_session_count BIGINT NULL 컬럼 추가
--   2. NULL = 신규 내담자 (외부 상담 이력 없음, 현 카운트 그대로 사용)
--   3. 0 이상 정수 = 외부(타 기관) 상담 회기수 — 모달·기타 화면에서 합산 표시
--
-- 운영 안전성:
--   - NULL 허용 — 기존 데이터 영향 0건
--   - 백필 없음 (정책: 신규 내담자 = NULL)
--   - 단순 컬럼 추가 — 인덱스 불필요 (집계 단순, 사용자 단건 조회 시 함께 로드)
--
-- FK 정합: 컬럼만 추가. FK 신설 없음.
--
-- 위임 출처: 메인 어시스턴트 위임서 (2026-06-04) §변경 매트릭스 §2.
--
-- @author MindGarden
-- @since 2026-06-08
-- =============================================================================

ALTER TABLE users
    ADD COLUMN past_session_count BIGINT NULL
    COMMENT '과거 회기수 (외부 상담 이력, NULL=신규 내담자, 0=명시적 0회)';
