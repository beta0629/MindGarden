const HOLIDAY_BG_CLASS = 'mg-v2-ad-calendar-event--kr-public-holiday-bg';

/** FullCalendar `dayCellClassNames` 등 — 로컬 달력 일(브라우저 TZ) 기준 ISO 키 조회 */
export function getKrPublicHolidayNameForLocalDate(date) {
  if (!(date instanceof Date) || Number.isNaN(date.getTime())) {
    return null;
  }
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  return KR_PUBLIC_HOLIDAYS_BY_ISO[`${y}-${m}-${d}`] || null;
}

/**
 * FullCalendar 이벤트 소스용 — 배경 레이어(display background), 비상호작용.
 * @returns {Array<object>}
 */
export function buildKrPublicHolidayFullCalendarEvents() {
  return Object.entries(KR_PUBLIC_HOLIDAYS_BY_ISO).map(([start, title]) => ({
    id: `kr-ph-${start}`,
    title,
    start,
    allDay: true,
    display: 'background',
    editable: false,
    startEditable: false,
    durationEditable: false,
    overlap: true,
    extendedProps: {
      type: CALENDAR_EXTENDED_TYPE_KR_PUBLIC_HOLIDAY,
      holidayName: title
    },
    classNames: [HOLIDAY_BG_CLASS]
  }));
}

/** 모듈 로드 시 1회 생성(스케줄 로드마다 새 배열 불필요) */
export const KR_PUBLIC_HOLIDAY_FULLCALENDAR_EVENTS = buildKrPublicHolidayFullCalendarEvents();
